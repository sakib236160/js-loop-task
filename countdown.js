/***

Implement a countdown timer that counts down from 21 to 15.

 */
let count = 21;

while (count >= 15) {
    console.log(count);
    count--;
}

// for loop using
/***

Implement a countdown timer that counts down from 81 to 65.

 */
for(let num = 81; num >= 65; num--){
    console.log('That counts down:', num);
}